function biggerSupermanImg(supermanImg) {
    supermanImg.width = "400";
    supermanImg.height = "505";
    document.getElementById("supermanFigCaption").style.width = "400px";
    return 0;
}

function smallerSupermanImg(supermanImg) {
    supermanImg.width = "260";
    supermanImg.height = "328";
    document.getElementById("supermanFigCaption").style.width = "260px";
    return 0;
}
