function biggerSupermanImg(supermanImg) {
    supermanImg.width = "400";
    supermanImg.height = "505";
    document.getElementById("supermanFigCaption").style.width = "400";
    return 0;
}

function smallerSupermanImg(supermanImg) {
    supermanImg.width = "300";
    supermanImg.height = "378";
    document.getElementById("supermanFigCaption").style.width = "300";
    return 0;
}
